#!/usr/bin/env bash
set -euo pipefail

MSG_FILE="$1"
SOURCE="${2:-}"

COAUTHOR_LINE="Co-authored-by: Codex <codex@openai.com>"

# ── Guard: bail out if Claude Code is driving the commit ────────────────────
# Claude Code sets AI_AGENT to "claude-code_*". It never sets Codex runtime
# flags (CODEX_THREAD_ID / CODEX_SHELL), but may inherit CODEX_COAUTHOR=1
# from a globally-persisted shell config. We skip the hook entirely so
# Claude Code commits never get a stray Codex co-author.
AI_AGENT="${AI_AGENT:-}"
if [[ "$AI_AGENT" == claude-code* ]]; then
  exit 0
fi

CODEX_SHELL_FLAG="${CODEX_SHELL:-}"
CODEX_THREAD_FLAG="${CODEX_THREAD_ID:-}"
CODEX_ORIGINATOR="${CODEX_INTERNAL_ORIGINATOR_OVERRIDE:-}"

# The original hook respected CODEX_COAUTHOR=1 as a standalone enable flag.
# That is too broad because users often export it globally in their shell
# config.  Now we require a runtime-only Codex signal (thread id, shell
# mode, or Codex Desktop originator) to prove Codex is actually calling
# `git commit`.  Keep CODEX_COAUTHOR=1 as an *additional* opt-in gate but
# never let it trigger the hook by itself.
ENABLE_FLAG="${CODEX_COAUTHOR:-}"
HAS_RUNTIME_SIGNAL=false
[[ "$CODEX_SHELL_FLAG" == "1" ]] && HAS_RUNTIME_SIGNAL=true
[[ -n "$CODEX_THREAD_FLAG" ]] && HAS_RUNTIME_SIGNAL=true
[[ "$CODEX_ORIGINATOR" == "Codex Desktop" ]] && HAS_RUNTIME_SIGNAL=true

if [[ "$HAS_RUNTIME_SIGNAL" != "true" ]]; then
  exit 0
fi

# Skip autogenerated commit messages where rewriting is noisy or surprising.
case "$SOURCE" in
  merge | squash)
    exit 0
    ;;
esac

if grep -Fqx "$COAUTHOR_LINE" "$MSG_FILE"; then
  exit 0
fi

python3 - "$MSG_FILE" "$COAUTHOR_LINE" <<'PY'
from pathlib import Path
import sys

msg_path = Path(sys.argv[1])
coauthor = sys.argv[2]
text = msg_path.read_text(encoding="utf-8")

stripped = text.rstrip("\n")
if not stripped:
    msg_path.write_text(coauthor + "\n", encoding="utf-8")
    raise SystemExit(0)

msg_path.write_text(stripped + "\n\n" + coauthor + "\n", encoding="utf-8")
PY
